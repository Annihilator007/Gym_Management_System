create database gym_management_system;
use gym_management_system;

CREATE TABLE STAFF (
NAMES VARCHAR(20) NOT NULL,
MOB_NO BIGINT NOT NULL,
SALARY INT NOT NULL,
STAFF_ID INT AUTO_INCREMENT PRIMARY KEY ,
EXPERIENCE INT NOT NULL,
LOGIN_ID VARCHAR(20) NOT NULL,
PASSWORDS VARCHAR(20) NOT NULL
);
CREATE TABLE DIET_PLAN (
DIET_ID INT PRIMARY KEY,
DESCRIPTIONS VARCHAR(20) NOT NULL
);
CREATE TABLE MEMBERSHIP_TYPE (
NAMES VARCHAR(20) NOT NULL,
TYPE_ID INT PRIMARY KEY,
MEMBERSHIP_PERIOD INT NOT NULL,
AMMOUNT INT NOT NULL
);

CREATE TABLE MEMBER (
MEMBER_ID INT PRIMARY KEY,
NAMES VARCHAR(20) NOT NULL,
AGE INT NOT NULL,
CONTACT BIGINT NOT NULL,
GENDER VARCHAR(1) NOT NULL,
JOINING_DATE DATE NOT NULL,
STAFF_ID INT NOT NULL,
DIET_ID INT NOT NULL,
TYPE_ID INT NOT NULL,
FOREIGN KEY(TYPE_ID) REFERENCES MEMBERSHIP_TYPE(TYPE_ID),
FOREIGN KEY(STAFF_ID) REFERENCES STAFF(STAFF_ID),
FOREIGN KEY(DIET_ID) REFERENCES DIET_PLAN(DIET_ID)
);
CREATE TABLE PAYMENT (
AMMOUNT INT NOT NULL,
PAYMENT_ID INT PRIMARY KEY,
MODE_OF_PAYMENT VARCHAR(20) NOT NULL,
STAFF_ID INT NOT NULL,
MEMBER_ID INT NOT NULL,
FOREIGN KEY(STAFF_ID) REFERENCES STAFF(STAFF_ID),
FOREIGN KEY(MEMBER_ID) REFERENCES MEMBER(MEMBER_ID)
);
CREATE TABLE INSTRUCTOR (
INSTRUCTOR_ID INT PRIMARY KEY,
CONTACT BIGINT NOT NULL,
SALARY INT NOT NULL,
ADDRESS VARCHAR(20) NOT NULL,
NAMES VARCHAR(20) NOT NULL,
STAFF_ID INT NOT NULL,
FOREIGN KEY(STAFF_ID) REFERENCES STAFF(STAFF_ID)
);
CREATE TABLE EQUIPMENT (
E_NAME VARCHAR(20) NOT NULL,
E_USED_FOR VARCHAR(20) NOT NULL,
E_MODELNO INT PRIMARY KEY,
INSTRUCTOR_ID INT NOT NULL,
FOREIGN KEY(INSTRUCTOR_ID) REFERENCES INSTRUCTOR(INSTRUCTOR_ID)
);
CREATE TABLE USES (
MEMBER_ID INT NOT NULL,
E_MODELNO INT NOT NULL,
FOREIGN KEY(MEMBER_ID) REFERENCES MEMBER(MEMBER_ID),
FOREIGN KEY(E_MODELNO) REFERENCES EQUIPMENT(E_MODELNO)
);
CREATE TABLE BODY_DETAILS (
WEIGHT INT NOT NULL,
HEIGHT INT NOT NULL,
MEMBER_ID INT PRIMARY KEY,
FOREIGN KEY(MEMBER_ID) REFERENCES MEMBER(MEMBER_ID)
);

